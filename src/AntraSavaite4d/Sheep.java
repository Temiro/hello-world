package AntraSavaite4d;

public class Sheep extends Animal {
    private String balsas = "Beee";


    @Override
    public String toString() {
        return getKlase() + " " + getBalsas();
    }

    public String getBalsas() {
        return balsas;
    }

    public void setBalsas(String balsas) {
        this.balsas = balsas;
    }
}